    private void jButton3SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3SalvarActionPerformed
        // TODO add your handling code here:
        if(jTextFieldcargo.isEnabled()){
           Funcionarios usu = new Funcionarios();
            
            if(jTextFieldcargo.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Insira o cargo do funcionario!");
                return;
            }
            else{
                usu.setCargo(jTextFieldcargo.getText());
            }
            if(jTextFieldnome.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Insira o nome do funcionario!");
                return;
            }
            else{
                usu.setNome(jTextFieldnome.getText());
            }
             if(jTextFieldendereco.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Insira o endere√ßo do funcionario!");
                return;
            }
            else{
                usu.setEndereco(jTextFieldendereco.getText());
            }
             if(jFormattedTextFieldTelefone.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Insira o telefone do funcionario!");
                return;
            }
            else{
                usu.setTelefone( jFormattedTextFieldTelefone.getText());
            }
             if(jTextFieldsalario.getText().equals("")){
                 JOptionPane.showMessageDialog(this,"Insira o salario do funcionario!");
                 return;
             }
             else{
                 usu.setSalario(jTextFieldsalario.getText());
             }
             
             funcionariosDAO.cadastrar(usu);
             carregaFuncionarios();
             jButton2CancelarActionPerformed(evt);
        }
    }//GEN-LAST:event_jButton3SalvarActionPerformed

